    pqe_1113 = params_cqe_d_1311_ee()
    cqe_1113=pqe_1113[i]
    pqe_1133 = params_cqe_d_3311_ee()
    cqe_1133=pqe_1133[i]


    events = (SM_ee[i] +
    clq_11*(clq_1111[1]*Vtd*Vtd+clq_1122[1]*Vts*Vts+clq_1133[1]*Vtb*Vtb) +
    clq_11*clq_11*(clq_1111[2]*Vtd*Vtd*Vtd*Vtd+clq_1112[2]*Vtd*Vts*Vtd*Vts+ clq_1113[2]*Vtd*Vtb*Vtd*Vtb+ clq_1122[2]*Vts*Vts*Vts*Vts+ clq_1123[2]*Vts*Vtb*Vts*Vtb+ clq_1133[2]*Vtb*Vtb*Vtb*Vtb) +
